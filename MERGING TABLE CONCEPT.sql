ITEM_ID INT,
ITEM_NAME VARCHAR(20),
ITEM_PRICE VARCHAR(20)
)

create table  DISTRIBUTAR(
ITEM_ID INT,
ITEM_NAME VARCHAR(20),
ITEM_PRICE VARCHAR(20)
)
create table  SHOP(
ITEM_ID INT,
ITEM_NAME VARCHAR(20),
ITEM_PRICE VARCHAR(20)
)

