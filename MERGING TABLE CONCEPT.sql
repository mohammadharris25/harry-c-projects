MERGE INTO DISTRIBUTAR DI
USING HAVELLS HA
ON(DI.ITEM_ID= HA.ITEM_ID)
WHEN NOT MATCHED BY TARGET 
THEN 
INSERT (ITEM_ID,ITEM_NAME,ITEM_PRICE)
VALUES (HA.ITEM_ID,HA.ITEM_NAME,HA.ITEM_PRICE)
WHEN MATCHED
THEN 
UPDATE  SET DI.ITEM_PRICE=HA.ITEM_PRICE;

MERGE INTO SHOP SH
USING DISTRIBUTAR DI
ON(SH.ITEM_ID= DI.ITEM_ID)
WHEN NOT MATCHED BY TARGET 
THEN 
INSERT (ITEM_ID,ITEM_NAME,ITEM_PRICE)
VALUES (DI.ITEM_ID,DI.ITEM_NAME,DI.ITEM_PRICE)
WHEN MATCHED
THEN 
UPDATE  SET SH.ITEM_PRICE=DI.ITEM_PRICE;


